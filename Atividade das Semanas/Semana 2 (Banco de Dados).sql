    else:
        classes_formatadas = "Nenhuma"


    # Pega inventário (se for None, vira lista vazia)
    inventario = info.get('Inventario') or []
    #limpa lista de itens formatados para não repetir
    itens_formatadas = ""
    if not inventario: itens_formatadas = "Vazio"
    else:
      for y in inventario:
          if y == "Vazio": #caso seja vazio, ele já encerra
              itens_formatadas = "Vazio"
              break
